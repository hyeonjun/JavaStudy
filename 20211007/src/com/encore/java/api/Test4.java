package com.encore.java.api;

public class Test4 {
	public static void main(String[] args) {
		System.out.println(Math.E);
		System.out.println(Math.PI);
		
		System.out.println(Math.abs(-12)); // 절대값
		System.out.println(Math.ceil(12.5)); // 올림
		System.out.println(Math.floor(12.8)); // 내림
		System.out.println(Math.round(12.5)); // 반올림
		System.out.println(Math.max(5, 8)); // 가장 큰 값
		System.out.println(Math.min(5, 8)); // 가장 작은 값
		System.out.println(Math.pow(2, 3)); // 2^3
		System.out.println(Math.random());
		System.out.println(Math.sqrt(4)); // 루트
	}
}
